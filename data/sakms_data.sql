-- Create database
CREATE DATABASE IF NOT EXISTS sakms_db;
USE sakms_db;

-- ============================================
-- TABLE 1: employees
-- ============================================
CREATE TABLE employees (
  employee_id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(100) NOT NULL UNIQUE,
  staff_id VARCHAR(50) UNIQUE,
  department VARCHAR(100),
  role VARCHAR(100),
  hire_date DATE,
  status ENUM('Active', 'Inactive') DEFAULT 'Active',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- TABLE 2: evaluation_periods
-- ============================================
CREATE TABLE evaluation_periods (
  period_id INT AUTO_INCREMENT PRIMARY KEY,
  year YEAR NOT NULL,
  start_date DATE,
  end_date DATE,
  UNIQUE(year)
);

-- ============================================
-- TABLE 3: kpi_master
-- ============================================
CREATE TABLE kpi_master (
  kpi_code VARCHAR(50) PRIMARY KEY,
  section VARCHAR(50) NOT NULL,
  kpi_group VARCHAR(100) NOT NULL,
  kpi_description TEXT,
  section_weight DECIMAL(5,3),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- TABLE 4: kpi_scores
-- ============================================
CREATE TABLE kpi_scores (
  score_id INT AUTO_INCREMENT PRIMARY KEY,
  employee_id INT NOT NULL,
  kpi_code VARCHAR(50) NOT NULL,
  evaluation_date DATE NOT NULL,
  score TINYINT CHECK (score >= 1 AND score <= 5),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (employee_id) REFERENCES employees(employee_id) ON DELETE CASCADE,
  FOREIGN KEY (kpi_code) REFERENCES kpi_master(kpi_code),
  INDEX idx_employee_date (employee_id, evaluation_date),
  INDEX idx_kpi_code (kpi_code)
);

-- ============================================
-- TABLE 5: supervisor_comments
-- ============================================
CREATE TABLE supervisor_comments (
  comment_id INT AUTO_INCREMENT PRIMARY KEY,
  employee_id INT NOT NULL,
  period_id INT NOT NULL,
  supervisor_comments TEXT,
  training_recommendations TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (employee_id) REFERENCES employees(employee_id) ON DELETE CASCADE,
  FOREIGN KEY (period_id) REFERENCES evaluation_periods(period_id),
  UNIQUE(employee_id, period_id)
);

-- ============================================
-- TABLE 6: rating_scale
-- ============================================
CREATE TABLE rating_scale (
  score TINYINT PRIMARY KEY,
  description VARCHAR(50) NOT NULL
);

